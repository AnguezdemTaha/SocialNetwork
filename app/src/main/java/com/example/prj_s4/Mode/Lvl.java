package com.example.prj_s4.Mode;

public class Lvl {
    private int lvl;
    private int exp4;

    public Lvl() {
    }

    public Lvl(int lvl, int exp4) {
        this.lvl = lvl;
        this.exp4 = exp4;
    }

    public int getLvl() {
        return lvl;
    }

    public void setLvl(int lvl) {
        this.lvl = lvl;
    }

    public int getExp4() {
        return exp4;
    }

    public void setExp4(int exp4) {
        this.exp4 = exp4;
    }
}
